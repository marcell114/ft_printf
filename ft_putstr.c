#include "libftprintf.h"

int ft_putstr(char *str)
{
	int	i;

	i = 0;
    if (str == NULL)
    {
        return (ft_putstr("(null)"));
    }
	while (*str)
	{
		i += ft_putchar((int)*str);
		str++;
	}
    return (i);
}
